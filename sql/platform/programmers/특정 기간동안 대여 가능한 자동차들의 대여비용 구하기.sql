### 📝 핵심 포인트 파헤치기: 날짜 겹침 (Date Overlap) 로직

이 문제에서 10명 중 9명이 실수하는 부분이 바로 "11월에 대여 가능한 차"를 찾는 로직입니다.

**"11월 1일 ~ 11월 30일에 대여가 불가능한 상황(겹침)"**은 다음 4가지 경우가 있습니다.

1. 대여 시작일이 11월 이전이고, 반납일이 11월 중인 경우
2. 대여 시작일이 11월 중이고, 반납일이 11월 이후인 경우
3. 대여 시작일과 반납일 모두 11월 중인 경우
4. 대여 시작일이 11월 이전이고, 반납일이 11월 이후인 경우 (11월 내내 렌트 중)

이 4가지를 `START_DATE BETWEEN ...` 형식으로 일일이 다 쓰면 코드가 엄청나게 길어지고 실수하기 쉽습니다.
이를 수학적으로 아주 깔끔하게 정리한 공식이 바로 아래 조건입니다!

> **`기준_시작일 <= 대여_종료일 AND 기준_종료일 >= 대여_시작일`**

우리의 기준일(11월 1일 ~ 30일)을 대입하면:
**`'2022-11-01' <= END_DATE AND '2022-11-30' >= START_DATE`** 가 됩니다.
서브쿼리에서 이 조건에 해당하는 `CAR_ID`를 찾아서 메인 쿼리에서 **`NOT IN`**으로 쏙 빼주면 완벽하게 "대여 가능한 차"만 남게 됩니다.


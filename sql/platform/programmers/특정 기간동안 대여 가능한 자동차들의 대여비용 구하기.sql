### 🔄 SQL 쿼리의 논리적 실행 순서 (작성 순서 vs 실행 순서)

우리가 쿼리를 작성할 때는 `SELECT`를 가장 먼저 쓰지만, 데이터베이스 엔진은 아래의 순서대로 일을 처리합니다.

| 실행 순서 | 명령어 | 역할 (데이터베이스의 생각 흐름) | 비고 |
| --- | --- | --- | --- |
| **1** | `FROM` / `JOIN` | **"어떤 테이블에서 데이터를 가져와서 어떻게 합칠까?"** | 대상 데이터 확보 |
| **2** | `WHERE` | **"조건에 맞지 않는 개별 행(Row)들은 버리자."** | 그룹화 **전** 필터링 |
| **3** | `GROUP BY` | **"남은 데이터들을 특정 기준에 따라 그룹으로 묶자."** | 데이터 압축 (바구니 담기) |
| **4** | `HAVING` | **"묶여진 그룹(바구니) 중에서 조건에 안 맞는 그룹은 버리자."** | 그룹화 **후** 필터링 |
| **5** | `SELECT` | **"최종적으로 어떤 컬럼(또는 계산된 결과)만 보여줄까?"** | 출력 데이터 확정 및 **별칭(Alias) 부여** |
| **6** | `ORDER BY` | **"결과를 어떤 기준으로 예쁘게 줄 세울까?"** | 최종 정렬 |
| **7** | `LIMIT` / `OFFSET` | **"위에서부터 몇 개만 짤라서 보여줄까?"** | 출력 개수 제한 |


### 💡 보너스 팁: MySQL만의 특별한 허용 (HAVING과 Alias)

표준 SQL 규칙에 따르면 `HAVING`(4단계) 역시 `SELECT`(5단계)보다 먼저 실행되므로 `SELECT`에서 만든 별칭을 `HAVING`에서 쓰면 에러가 나야 정상입니다.

**하지만!** 우리가 주로 사용하는 **MySQL**은 개발자의 편의를 위해 **`HAVING` 절이나 `GROUP BY` 절에서 `SELECT`의 별칭을 사용할 수 있도록 예외적으로 허용**해주고 있습니다. (그래서 앞선 렌터카 문제에서 `HAVING FEE >= 500000` 이 정상적으로 작동했던 것입니다.)

다만, Oracle 등 다른 데이터베이스에서는 엄격하게 에러를 뱉어내므로, 원칙적인 실행 순서를 이해해 두는 것이 진짜 실력입니다!


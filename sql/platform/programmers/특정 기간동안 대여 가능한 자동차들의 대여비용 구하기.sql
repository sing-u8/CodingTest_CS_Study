### 🚨 코딩 테스트에서 절대 잊지 말아야 할 3가지 법칙

이 순서를 알면 왜 특정 문법이 에러를 내는지 정확히 이해할 수 있습니다.

#### 1. `WHERE` 절에서는 그룹 함수(`SUM`, `AVG`, `COUNT` 등)를 쓸 수 없다!

* **이유:** `WHERE`(2단계)는 아직 데이터를 그룹으로 묶기(`GROUP BY`, 3단계) **전**에 실행됩니다. 아직 평균이나 합계가 계산되지도 않았는데 필터링을 하려고 하니 에러가 나는 것입니다.
* **해결책:** 그룹화된 결과를 필터링하려면 반드시 4단계인 **`HAVING`**을 써야 합니다.

#### 2. `SELECT`에서 만든 별칭(Alias)은 `WHERE`에서 쓸 수 없다!

* **이유:** 방금 전 렌터카 문제에서 `ROUND(...) AS FEE` 라고 별칭을 지어줬죠? 하지만 `WHERE` 절(2단계)이 실행될 때는 아직 `SELECT` 절(5단계)이 실행되기 전이라 데이터베이스는 `FEE`라는 이름이 세상에 존재하는지조차 모릅니다.
* **해결책:** `WHERE` 절에서는 원래의 계산식(`ROUND(...)`)을 그대로 쓰거나, 인라인 뷰(서브쿼리)로 한 번 감싸야 합니다.

#### 3. `SELECT`에서 만든 별칭은 `ORDER BY`에서는 쓸 수 있다!

* **이유:** `ORDER BY`(6단계)는 `SELECT`(5단계) **이후**에 실행됩니다! 따라서 `SELECT`에서 지어준 별칭을 `ORDER BY FEE DESC` 처럼 아주 자연스럽게 가져다 쓸 수 있습니다.


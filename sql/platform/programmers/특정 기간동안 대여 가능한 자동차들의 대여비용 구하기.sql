### 💡 문제 해결의 3단계 핵심 논리

1. **날짜 필터링 (가장 많이 틀리는 부분 🚨)**:
2022년 11월 1일부터 11월 30일까지 **대여가 가능한** 차를 찾아야 합니다. 즉, 대여 기록(`RENTAL_HISTORY`) 중에서 **이 기간과 단 하루라도 겹치는 기록이 있는 차를 제외(`NOT IN`)** 시켜야 합니다.
* *겹치는 조건:* `START_DATE <= '2022-11-30' AND END_DATE >= '2022-11-01'`


2. **할인율 적용 (`JOIN`)**:
자동차 종류가 '세단' 또는 'SUV'이면서, 30일간 빌릴 것이므로 요금 할인 정책 테이블에서 `DURATION_TYPE = '30일 이상'`인 할인율을 찾아 원본 테이블과 조인합니다.
3. **요금 계산 및 필터링 (`HAVING`)**:
일일 대여 요금에 30일을 곱하고, 할인율을 적용하여 총 요금(`FEE`)을 계산합니다. 이 값이 50만 원 이상, 200만 원 미만인지 필터링합니다.

